package practice2;
//Write a java program to check if the number is odd or even.

public class OddOrEven {
    public static void main(String[] args) {
        int myNum = 5;
        if (myNum % 2 == 0) {
            System.out.println("Num is Even");
        } else {
            System.out.println("Num is odd");
        }
    }
}
